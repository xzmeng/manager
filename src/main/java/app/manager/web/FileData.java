package app.manager.web;

public class FileData {

    private String institute;

    private String major;

    private String admissionInfo;

    private String jobInfo;

    private String teachingQuality;

    private String constructFile;

    private String note;

    public String getInstitute() {
        return institute;
    }

    public void setInstitute(String institute) {
        this.institute = institute;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getAdmissionInfo() {
        return admissionInfo;
    }

    public void setAdmissionInfo(String admissionInfo) {
        this.admissionInfo = admissionInfo;
    }

    public String getJobInfo() {
        return jobInfo;
    }

    public void setJobInfo(String jobInfo) {
        this.jobInfo = jobInfo;
    }

    public String getTeachingQuality() {
        return teachingQuality;
    }

    public void setTeachingQuality(String teachingQuality) {
        this.teachingQuality = teachingQuality;
    }

    public String getConstructFile() {
        return constructFile;
    }

    public void setConstructFile(String constructFile) {
        this.constructFile = constructFile;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
